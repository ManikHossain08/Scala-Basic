    var results = collection.Where( num => num % 2 != 0);

    A simple example in Python:

    # Declarative
    small_nums = [x for x in range(20) if x < 5]

    # Imperative
    small_nums = []
    for i in range(20):
        if i < 5:
          small_nums.append(i)
